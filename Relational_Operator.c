#include<stdio.h>
int main()
{
    // Equal to operator(==)
    printf("%d \n", 4==4);

    // Less than operator or less than equal to operator(<,<=)
    printf("%d \n", 4<3);
    printf("%d \n", 3<4);
    printf("%d \n", 4<4);
    printf("%d \n", 4<=4);


    // Greater than operator or Grater than equal to operator(>,>=)
    printf("%d \n", 4>3);
    printf("%d \n", 3>4);
    printf("%d \n", 4>4);
    printf("%d \n", 4>=4);

    // Not Equal to operator (!=)
    printf("%d \n", 4!=4);
    printf("%d \n", 3!=4);

}